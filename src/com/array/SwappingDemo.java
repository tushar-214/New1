package com.array;

public class SwappingDemo {
	public static void main(String[] args) {
		int a=10,b=20;
		
		System.out.println("value of A "+a);
		System.out.println("value of B "+b);
		
		
		
		int temp=a;
		a=b;
		b=temp;
		System.out.println("------------------------------------------");
		
		System.out.println("value of A "+a);
		System.out.println("value of B "+b);
		
	}

}
